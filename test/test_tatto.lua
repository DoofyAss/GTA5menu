function tatto.tick()

	if(SYSTEM.TIMERB() > 120 and tatto_render) then
		if(get_key_pressed(Keys.Up)) then				-- Up
			if(cur_item == #a) then cur_item = 1 else cur_item = cur_item + 1 end
			setTatto()
			SYSTEM.SETTIMERB(0)
		end
		
		if(get_key_pressed(Keys.Down)) then				-- Down
			if(cur_item == 1) then cur_item = #a else cur_item = cur_item - 1 end
			setTatto()
			SYSTEM.SETTIMERB(0)
		end
	end
	
	if(get_key_pressed(Keys.Z)) then
		if(tatto_render) then tatto_render = false else tatto_render = true end
		wait(200)
	end
	
	render()

end

function setTatto()
	PED.CLEAR_PED_DECORATIONS(getPlayer())
	PED._0x5F5D1665E352A839(PLAYER.PLAYER_PED_ID(), GAMEPLAY.GET_HASH_KEY("multiplayer_overlays"), GAMEPLAY.GET_HASH_KEY(a[cur_item]))
end

function render()

	if(tatto_render) then
	
		CONTROLS.DISABLE_CONTROL_ACTION(2, 27, true) -- INPUT_PHONE (Up)
		
		-- Head
		GRAPHICS.DRAW_RECT(0.07, 0.019, 2.0, 0.038, 0, 0, 0, 200)
		drawText("("..cur_item.." of "..#a..") "..a[cur_item], 0, 0.32, 0.006, 0.0055, false)
	
	end

end

return tatto
